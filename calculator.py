
def add(a,b):
    z = a + b
    return z


def sub(a,b):
    z = a - b
    return z


def mul(a,b):
    z = a * b
    return z


def div(a,b):
    z = a / b
    return z


def divEven(a,b):
    z = a // b
    return z


def modulo(a,b):
    z = a % b
    return z


def sqrt(a):
    while a >= 0:
        z = a ** 0.5
        return z
    return 'Invalid Number'

